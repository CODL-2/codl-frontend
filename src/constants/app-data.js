export const getLanguageIcon = (language) => {
  switch (language) {
    case "javascript":
      return images.javaScript;
    case "cpp":
      return images.cPlusPlus;
    case "typescript":
      return images.typeScript;
    case "java":
      return images.java;
    case "python":
      return images.python;
    case "kotlin":
      return images.kotlin;
    case "ruby":
      return images?.ruby;
    case "csharp":
      return images.cSharp;
    case "php":
      return images?.php;
    case "scala":
      return images.scala;
    case "elixir":
      return images?.elixir;
    default:
      break;
  }
};
